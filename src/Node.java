public class Node{
    int num;
    String name;

    public Node(){
    }

    public Node(int num, String name){
        this.num = num;
        this.name = name;
    }


    public void setNode(int num, String name){
        this.num = num;
        this.name = name;
    }

    public void setNode(int n){
        this.num = n;
    }

}
